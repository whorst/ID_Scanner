
public class Employee {

	String name;
	int Salary;
	
	public Employee(String name, int Salary){
		this.name=name;
		this.Salary=Salary;
	}
	

	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getSalary() {
		return Salary;
	}
	public void setSalary(int salary) {
		Salary = salary;
	}

}
